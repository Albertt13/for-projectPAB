public class ModelClub {

    public String id, nama, julukan, tahun_berdiri, sejarah, pemilik, title, foto;

    public String getId() {
        return id;
    }

    public String getNama() {
        return nama;
    }

    public String getJulukan() {
        return julukan;
    }

    public String getTahun_berdiri() {
        return tahun_berdiri;
    }

    public String getSejarah() {
        return sejarah;
    }

    public String getPemilik() {
        return pemilik;
    }

    public String getTitle() {
        return title;
    }

    public String getFoto() {
        return foto;
    }
}
